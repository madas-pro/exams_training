#include <stdio.h>

unsigned int	lcm(unsigned int a, unsigned int b)
{
	int	i;

	if (a == 0 || b == 0)
		return (0);
	i = 1;
	while (42)
	{
		if (i % a == 0 && i % b == 0)
			return (i);
		i++;
	}
}
